# a set is a collection of element where each element is unique #
set1={1,2,4,"Jawad",(8,5,6)}
print(set1)
# add operation in set#
set1.add(5)
print(set1)
# update function in set#
set1.update([3]) #[] is used to update a set#
print(set1)
# remove function in set#
set1.remove(1)
print(set1)
